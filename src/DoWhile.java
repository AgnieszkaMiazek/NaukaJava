public class DoWhile {
    public static void main(String[] args) {
        int number = 21;
        do {
            System.out.println("Wartość odżywcza to: " + " i jest mniejsza od 20");
            number++;
        }  while (number<20) ;
    }
}
// w pętli "do while" wykonujemy kod nawet jeśli warunek nie jest spełniony, wykonujemy kod jednokrotnie a później następuje spradzenie